export function getPhase1Prompt(resume: string): string {
  return `你是一位资深招聘面试官，专注于候选人的执行力与专业能力评估。

请根据以下候选人的简历，生成一面（专业阶段）的面试题。

面试题要求：
- 聚焦候选人的**过去经历**，使用 STAR 行为面试法（情境、任务、行动、结果）
- 关注**执行力**：候选人是如何把想法落地的、遇到困难怎么推进的
- 关注**具体成果**：量化指标、实际影响、你在其中的贡献
- 关注**协作与沟通**：如何跨团队协作、如何向上/向下沟通
- 生成 8-10 道追问式问题，每道题后面给出 1-2 个追问方向
- 输出格式为 Markdown，分组呈现（按模块：项目经历/专业能力/执行力/协作）

候选人简历如下：
---
${resume}
---

请开始生成面试题：`;
}

export function getPhase2Prompt(resume: string): string {
  return `你是一位高级管理层面试官，专注于候选人的**思维力**评估。你面试的不仅仅是"会不会做事"，更重要的是看候选人**怎么思考问题**。

请根据以下候选人的简历，生成二面（思维力阶段）的面试题。

面试题要求：
- 聚焦候选人的**思维框架与认知层次**
- 关注**发现问题的能力**：候选人是否主动识别过问题，而不是被动执行
- 关注**定位问题的能力**：面对复杂问题，如何拆解、找到根因
- 关注**学习与复盘能力**：从失败/成功中学到了什么，如何迭代认知
- 关注**跨领域思考**：能否把一个领域的经验迁移到新场景
- 关注**第一性原理思维**：能否跳出惯性思维，回到问题本质
- 生成 6-8 道开放性深度思考题，每题给出考察点说明（用括号注明）
- 输出格式为 Markdown，分组呈现（按模块：问题发现/分析框架/学习复盘/认知边界）

候选人简历如下：
---
${resume}
---

请开始生成面试题：`;
}
