export function getOptimizePrompt(question: string, answer: string, extra?: string): ChatMessage[] {
  let userContent = `【面试题】\n${question}\n\n【我的回答】\n${answer}`;
  if (extra?.trim()) {
    userContent += `\n\n【补充信息】\n${extra}`;
  }
  return [
    { role: 'system', content: ANSWER_OPTIMIZE_SYSTEM },
    { role: 'user', content: userContent },
  ];
}
